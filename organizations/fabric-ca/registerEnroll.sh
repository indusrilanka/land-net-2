#!/bin/bash

function createLandRegistry() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/land-registry.ousl.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/land-registry.ousl.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:5054 --caname ca-land-registry --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-5054-ca-land-registry.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-5054-ca-land-registry.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-5054-ca-land-registry.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-5054-ca-land-registry.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy land-registry's CA cert to land-registry's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/tlscacerts/ca.crt"

  # Copy land-registry's CA cert to land-registry's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/tlsca/tlsca.land-registry.ousl.com-cert.pem"

  # Copy land-registry's CA cert to land-registry's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/ca"
  cp "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/ca/ca.land-registry.ousl.com-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-land-registry --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-land-registry --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-land-registry --id.name lradmin --id.secret lradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:5054 --caname ca-land-registry -M "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/msp" --csr.hosts peer0.land-registry.ousl.com --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:5054 --caname ca-land-registry -M "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls" --enrollment.profile tls --csr.hosts peer0.land-registry.ousl.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/peers/peer0.land-registry.ousl.com/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:5054 --caname ca-land-registry -M "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/users/User1@land-registry.ousl.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/users/User1@land-registry.ousl.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://lradmin:lradminpw@localhost:5054 --caname ca-land-registry -M "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/users/Admin@land-registry.ousl.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/land-registry/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/land-registry.ousl.com/users/Admin@land-registry.ousl.com/msp/config.yaml"
}

